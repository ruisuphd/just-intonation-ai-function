#!/usr/bin/env python3
"""
Neural + Classical ensemble key detector.

Combines neural model softmax outputs with classical profile-based correlation
scores using a validation-tuned weighting parameter alpha:

    final_key = argmax(alpha * neural_softmax + (1 - alpha) * classical_dist)

The ensemble exploits complementary strengths: neural models capture learned
local tonal patterns while classical algorithms encode explicit music theory
knowledge from key profiles.

IMPORTANT: Requires prediction files with softmax probabilities (generated by
evaluate_harmonic_context_model.py with --save-predictions). Without softmax,
the neural side falls back to one-hot predictions which limits blending quality.

Usage:
    python ensemble_key_detector.py \
        --neural-predictions research_data/ablation_A1_predictions.json \
        --splits research_data/composition_splits.json
"""

from __future__ import annotations

import argparse
import json
import os
from typing import Dict, List, Sequence, Tuple


def _resolve_label_path(label_dirs: Sequence[str], cid_int: int) -> str | None:
    """Search label_dirs in order for a per-composition label file.

    Returns the first matching path, or None if not found. Mirrors the
    training-time search order used in load_records_from_manifest().
    """
    filename = f'{cid_int:04d}.json'
    for d in label_dirs:
        candidate = os.path.join(d, filename)
        if os.path.isfile(candidate):
            return candidate
    return None


def _coerce_label_dirs(label_dirs, label_dir: str | None = None) -> List[str]:
    """Accept either a list, a comma-separated string, a single dir, or None."""
    if label_dirs is None or label_dirs == '':
        return [label_dir] if label_dir else []
    if isinstance(label_dirs, str):
        return [d.strip() for d in label_dirs.split(',') if d.strip()]
    return list(label_dirs)

